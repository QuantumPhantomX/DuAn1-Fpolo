    private void cboFilterTrangThaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboFilterTrangThaiActionPerformed
        String selectedTrangThai = (String) cboFilterTrangThai.getSelectedItem();
        ArrayList<DetailSanPham> filteredProducts;

        switch (selectedTrangThai.toLowerCase()) {
            case "tất cả":
                filteredProducts = rp.getAll();
                break;
            case "còn hàng":
                filteredProducts = rp.getDetailProductsByTrangThai(true);
                break;
            case "hết hàng":
                filteredProducts = rp.getDetailProductsByTrangThai(false);
                break;
            default:
                filteredProducts = new ArrayList<>();
                break;
        }
        fillTable(filteredProducts);
    }//GEN-LAST:event_cboFilterTrangThaiActionPerformed

