    private boolean isValidDetailProductData() {
        if (txtTenSP.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên sản phẩm không được để trống.");
            return false;
        }

        try {
            BigDecimal giaBan = new BigDecimal(txtGiaBan.getText());
            if (giaBan.compareTo(BigDecimal.ZERO) <= 0) {
                JOptionPane.showMessageDialog(this, "Giá bán phải lớn hơn 0.");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Giá bán không hợp lệ.");
            return false;
        }

        try {
            int soLuongTon = Integer.parseInt(txtSoLuong.getText());
            if (soLuongTon < 0) {
                JOptionPane.showMessageDialog(this, "Số lượng tồn phải lớn hơn hoặc bằng 0.");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng tồn không hợp lệ.");
            return false;
        }

        if (cboMauSac.getSelectedIndex() == -1 || cboSize.getSelectedIndex() == -1
                || cboChatLieu.getSelectedIndex() == -1 || cboThuongHieu.getSelectedIndex() == -1
                || cboLoaiCoCao.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn đầy đủ thông tin thuộc tính.");
            return false;
        }

        return true;
    }
