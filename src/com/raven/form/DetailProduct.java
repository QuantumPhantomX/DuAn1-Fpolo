    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        if (isValidDetailProductData()) {
            try {
                String tenSanPham = txtTenSP.getText();
                String idSanPham = txtMaSP.getText();
                String moTa = txtMoTaCTSP.getText();

                int idMauSac = colorMap.get(cboMauSac.getSelectedItem().toString());
                int idSize = sizeMap.get(cboSize.getSelectedItem().toString());
                int idChatLieu = chatLieuMap.get(cboChatLieu.getSelectedItem().toString());
                int idThuongHieu = thuongHieuMap.get(cboThuongHieu.getSelectedItem().toString());
                int idLoaiCoCao = loaiCoCaoMap.get(cboLoaiCoCao.getSelectedItem().toString());

                BigDecimal giaBan = new BigDecimal(txtGiaBan.getText());
                int soLuongTon = Integer.parseInt(txtSoLuong.getText());

                DetailSanPham detailSanPham = new DetailSanPham(
                        null, idSanPham, tenSanPham, idMauSac, idSize, idChatLieu,
                        idThuongHieu, idLoaiCoCao, giaBan, soLuongTon, moTa
                );

                if (rp.addDetailProduct(detailSanPham)) {
                    JOptionPane.showMessageDialog(this, "Thêm sản phẩm chi tiết thành công!");
                    fillTable(rp.getAll());
                    clearForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm sản phẩm chi tiết thất bại!");
                }

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Giá bán và số lượng tồn phải là số!");
            } catch (NullPointerException ex) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn đầy đủ thông tin!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Lỗi: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btn_themActionPerformed
