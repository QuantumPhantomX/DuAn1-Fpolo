    private void txtTimKiemHDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKiemHDKeyReleased
        try {
            String keywordText = txtTimKiemHD.getText().trim();
            if (keywordText.isEmpty()) {
                fillTable(rp.getAll());
            } else {
                int keyword = Integer.parseInt(keywordText);
                ArrayList<HoaDon> filteredProducts = rp.timKiemHoaDonTheoMa(keyword);
                fillTable(filteredProducts);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Mã hóa đơn không hợp lệ!");
        }
    }//GEN-LAST:event_txtTimKiemHDKeyReleased

    private void tblHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHoaDonMouseClicked
        int selectedRow = tblHoaDon.getSelectedRow();
        if (selectedRow != -1) {
            int idHoaDon = (int) tblHoaDon.getValueAt(selectedRow, 0);
            Repository_HoaDonChiTiet re = new Repository_HoaDonChiTiet();
            ArrayList<HoaDonChiTiet> listHoaDonChiTiet = re.getHoaDonChiTietByIdHoaDon(idHoaDon);
            DefaultTableModel model = (DefaultTableModel) tblHoaDonChiTiet.getModel();
            model.setRowCount(0);
            for (HoaDonChiTiet hdct : listHoaDonChiTiet) {
                model.addRow(hdct.toDataRow());
            }
        }

    }//GEN-LAST:event_tblHoaDonMouseClicked

