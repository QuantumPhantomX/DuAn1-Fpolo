    private void btnLocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocActionPerformed

        String trangThai = (String) cboTrangThaiHD.getSelectedItem();
        String hinhThucThanhToan = (String) cboHinhThucThanhToan.getSelectedItem();

        java.sql.Date sqlNgayBatDau = null;
        java.sql.Date sqlNgayKetThuc = null;
        if (dateBatDau.getDate() != null) {
            sqlNgayBatDau = new java.sql.Date(dateBatDau.getDate().getTime());
        }
        if (dateKetThuc.getDate() != null) {
            sqlNgayKetThuc = new java.sql.Date(dateKetThuc.getDate().getTime());
        }

        ArrayList<HoaDon> listHoaDon = rp.timKiemHoaDon( trangThai, hinhThucThanhToan, sqlNgayBatDau, sqlNgayKetThuc);

        DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);
        for (HoaDon hd : listHoaDon) {
            model.addRow(hd.toDataRow());
        }
    }//GEN-LAST:event_btnLocActionPerformed

